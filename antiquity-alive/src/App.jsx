function searchAll(q) {
    if (!q.trim()) return [];
    const l = q.toLowerCase();
    const res = [];
    PERSONS.forEach(p => { if ([p.name, p.shortDesc, p.role].some(s => s.toLowerCase().includes(l))) res.push({ type: 'person', label: 'Личность', emoji: p.emoji, id: p.id, title: p.name, desc: p.shortDesc }); });
    ARTIFACTS.forEach(a => { if ([a.name, a.shortDesc, a.type].some(s => s.toLowerCase().includes(l))) res.push({ type: 'artifact', label: 'Артефакт', emoji: a.emoji, id: a.id, title: a.name, desc: a.shortDesc }); });
    REGIONS.forEach(r => { if ([r.name, r.shortDesc].some(s => s.toLowerCase().includes(l))) res.push({ type: 'region', label: 'Цивилизация', emoji: r.emoji, id: r.id, title: r.name, desc: r.shortDesc }); });
    ARTICLES.forEach(a => { if ([a.title, a.shortDesc, a.category].some(s => s.toLowerCase().includes(l))) res.push({ type: 'article', label: 'Статья', emoji: a.emoji, id: a.id, title: a.title, desc: a.shortDesc }); });
    TIMELINE.forEach(e => { if ([e.title, e.desc].some(s => s.toLowerCase().includes(l))) res.push({ type: 'timeline', label: 'Событие', emoji: '📅', id: `ev${e.id}`, title: e.title, desc: `${e.year} — ${e.desc}` }); });
    return res;
}


function Navbar({ page, setPage, searchQuery, setSearchQuery, onSearch }) {
    const nav = [['home', 'Главная'], ['timeline', 'Хронология'], ['persons', 'Личности'], ['artifacts', 'Артефакты'], ['regions', 'Цивилизации'], ['articles', 'Статьи']];
    const handleKey = e => { if (e.key === 'Enter' && searchQuery.trim()) onSearch(); };
    return (
        <nav className="navbar">
            <div className="navbar__inner">
                <span className="navbar__logo" onClick={() => setPage('home')}>
                    Antiquity <span>Alive</span>
                </span>
                <ul className="navbar__links">
                    {nav.map(([id, label]) => (
                        <li key={id}><button className={page === id ? 'active' : ''} onClick={() => setPage(id)}>{label}</button></li>
                    ))}
                </ul>
                <div className="navbar__search">
                    <input
                        type="text" placeholder="Поиск…"
                        value={searchQuery} onChange={e => setSearchQuery(e.target.value)}
                        onKeyDown={handleKey}
                    />
                    <button onClick={() => { if (searchQuery.trim()) onSearch(); }}>🔍</button>
                </div>
            </div>
        </nav>
    );
}

